const ll mod = 1e9 + 7;

ll powmod(ll a, ll e) {
  ll sum = 1;
  ll cur = a;
  while (e > 0) {
    if (e % 2) {
      sum = sum * cur % mod;
    }
    cur = cur * cur % mod;
    e /= 2;
  }
  return sum;
}


int main(void) {
  ll n;
  cin >> n;
  ll prod = 1;
  if (n >= 2) {
    prod = 4 * powmod(5, n / 2 - 1) % mod;
  }
  if (n % 2 == 1) {
    if (n >= 3) {
      prod = prod * 3 % mod;
    } else {
      prod = 2;
    }
  }
  cout << prod << endl;
}
